//display_variables

#include <stdio.h>
int main(void) {
    
   // variable declaration 
    int a = 4;
    char c = 'a';
    float f = 1.0;
    double d = 23344.888888;

// display variables
    printf("a = %d\n", a);
    printf("c = %c\n", c);
    printf("f = %f\n", f);
    printf("d = %lf\n", d);

    return 0;
}
